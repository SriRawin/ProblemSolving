def diagonalDifference(arr):
    # Write your code here
    rows=len(arr)
   
    diagonal_1=0
    diagonal_2=0
    
    for i in range(rows):
        diagonal_1+=arr[i][i]
        diagonal_2+=arr[i][(rows-1)-i]
   
    difference=abs(diagonal_1-diagonal_2)
    return difference

